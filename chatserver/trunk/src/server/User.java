package server;

public class User
{
	private String name;
	private String currentRoom;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCurrentRoom() {
		return currentRoom;
	}
	public void setCurrentRoom(String currentRoom) {
		this.currentRoom = currentRoom;
	}
	
}